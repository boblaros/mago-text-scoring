class BiLSTMAttention(torch.nn.Module):
    def __init__(self, input_dim=50, hidden_dim=128, output_dim=3, n_layers=2, bidirectional=True, dropout=0.5):
        super().__init__()
        # Dummy implementation for now
        self.lstm = torch.nn.LSTM(input_dim, hidden_dim, num_layers=n_layers,
                                  bidirectional=bidirectional, dropout=dropout, batch_first=True)
        self.fc = torch.nn.Linear(hidden_dim * 2 if bidirectional else hidden_dim, output_dim)

    def forward(self, x):
        # For dummy, assume x is already embeddings of shape [batch_size, seq_len, input_dim]
        outputs, (hidden, cell) = self.lstm(x)
        # Simple pooling or use the last hidden state from BiLSTM
        hidden = torch.cat((hidden[-2,:,:], hidden[-1,:,:]), dim=1) if self.lstm.bidirectional else hidden[-1,:,:]
        return self.fc(hidden)

class TextCNN(torch.nn.Module):
    def __init__(self, vocab_size=10, embedding_dim=50, n_filters=100, filter_sizes=[3,4,5], output_dim=3, dropout=0.5):
        super().__init__()
        # Dummy implementation for now
        self.embedding = torch.nn.Embedding(vocab_size, embedding_dim)
        self.convs = torch.nn.ModuleList([
            torch.nn.Conv2d(in_channels=1, out_channels=n_filters, kernel_size=(fs, embedding_dim))
            for fs in filter_sizes
        ])
        self.fc = torch.nn.Linear(len(filter_sizes) * n_filters, output_dim)
        self.dropout = torch.nn.Dropout(dropout)

    def forward(self, x):
        # For dummy, assume x is token IDs of shape [batch_size, seq_len]
        embedded = self.embedding(x)
        embedded = embedded.unsqueeze(1) # [batch_size, 1, seq_len, embedding_dim]
        conved = [torch.nn.functional.relu(conv(embedded)).squeeze(3) for conv in self.convs]
        pooled = [torch.nn.functional.max_pool1d(conv, conv.shape[2]).squeeze(2) for conv in conved]
        cat = self.dropout(torch.cat(pooled, dim=1))
        return self.fc(cat)



