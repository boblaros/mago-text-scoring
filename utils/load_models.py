def load_transformer_model(model_dir=None, device=None):
    """
    Load Transformer model + tokenizer from Drive using Hugging Face pipeline.
    Returns a ready-to-use text-classification pipeline.
    """
    device = device or ("cuda" if torch.cuda.is_available() else "cpu")
    model_dir = model_dir or DRIVE_MODEL_DIR / "transformer_best"

    # Load tokenizer and model
    tokenizer = AutoTokenizer.from_pretrained(str(model_dir))
    model = AutoModelForSequenceClassification.from_pretrained(str(model_dir))

    # Create pipeline (device=0 for CUDA, -1 for CPU)
    pipe = pipeline("text-classification", model=model, tokenizer=tokenizer, device=0 if device=="cuda" else -1)
    return pipe

