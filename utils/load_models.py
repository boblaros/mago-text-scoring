@torch.no_grad()
def infer_torch_model(model, dataloader, device):
    """
    Perform inference on a PyTorch model given a DataLoader.
    Returns predictions as a list of integers.
    """
    all_preds = []
    for batch in dataloader:
        inputs = batch["inputs"].to(device)
        outputs = model(inputs)
        preds = outputs.argmax(dim=1)
        all_preds.extend(preds.cpu().numpy())
    return all_preds

